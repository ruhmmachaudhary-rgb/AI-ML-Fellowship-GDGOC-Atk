# utils.py

def greet(name):
    """Greet a user"""
    return f"Hello, {name}!"

def add(a, b):
    """Return sum of two numbers"""
    return a + b

def multiply(a, b):
    """Return product of two numbers"""
    return a * b

def is_even(num):
    """Check if number is even"""
    return num % 2 == 0

